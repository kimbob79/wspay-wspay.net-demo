1)계약 또는 청약철회 등에 관한 기록 보존 이유 : 전자상거래 등에서의 소비자보호에 관한 법률
보존 기간 : 5년
2)대금결제 및 재화 등의 공급에 관한 기록 보존 이유 : 전자상거래 등에서의 소비자보호에 관한 법률
보존 기간 : 5년
3)전자금융 거래에 관한 기록 보존 이유 : 전자금융거래법
보존 기간 : 5년
4)세금계산서 발행에 관한 기록 보존 이유 : 부가가치세법
